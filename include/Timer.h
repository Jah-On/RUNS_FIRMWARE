/*

Useful constants for MSP-432 MCU timers

*/

// ------------------ CTL CONSTANTS ------------------ //
// TASSEL Sources
#define  CTL_TASSEL_TA 0b0000000000000000
#define  CTL_TASSEL_AC 0b0000000100000000
#define  CTL_TASSEL_SM 0b0000001000000000
#define  CTL_TASSEL_IN 0b0000001100000000

// CLK Dividers
#define  CTL_DIVIDER_1 0b0000000000000000
#define  CTL_DIVIDER_2 0b0000000001000000
#define  CTL_DIVIDER_4 0b0000000010000000
#define  CTL_DIVIDER_8 0b0000000011000000

// MC Modes
#define  CTL_MC_STOP   0b0000000000000000
#define  CTL_MC_UP     0b0000000000010000
#define  CTL_MC_CONT   0b0000000000100000
#define  CTL_MC_UPDOWN 0b0000000000110000

// CLear Flag
#define  CTL_CLR_FALSE 0b0000000000000000
#define  CTL_CLR_TRUE  0b0000000000000100

// Interrupt Enable Flag
#define  CTL_INTR_OFF  0b0000000000000000
#define  CTL_INTR_ON   0b0000000000000010

// Interrupt PEND Flag
#define  CTL_PEND_NO   0b0000000000000000
#define  CTL_PEND_YES  0b0000000000000001

#define  CTL_HIGH_RSVD 0b1111110000001000
// --------------------------------------------------- //

// ------------------ CCTL CONSTANTS ----------------- //
// Capture Modes
#define  CCTL_CM_NONE        0b0000000000000000
#define  CCTL_CM_RISING      0b0100000000000000
#define  CCTL_CM_FALLING     0b1000000000000000
#define  CCTL_CM_BOTH        0b1100000000000000

// Input Select
#define  CCTL_CCIS_A         0b0000000000000000
#define  CCTL_CCIS_B         0b0001000000000000
#define  CCTL_CCIS_GND       0b0010000000000000
#define  CCTL_CCIS_VCC       0b0011000000000000

// Sync Source
#define  CCTL_SCS_ASYNC      0b0000000000000000
#define  CCTL_SCS_SYNC       0b0000100000000000

// Sync Source Input
#define  CCTL_SCIS           0b0000000000000000

// Sync Capture Input
#define  CCTL_SCCI           0b0000000000000000

// Reserved bit
#define  CCTL_HIGH_RSVD      0b0000001000000000

// Capture Mode
#define  CCTL_CAP_COMP       0b0000000000000000
#define  CCTL_CAP_CAPT       0b0000000100000000

// Out Modes
#define  CCTL_OUTMOD_BIT_VAL 0b0000000000000000
#define  CCTL_OUTMOD_SET     0b0000000000100000
#define  CCTL_OUTMOD_TGL_RST 0b0000000001000000
#define  CCTL_OUTMOD_SET_RST 0b0000000001100000
#define  CCTL_OUTMOD_TGL     0b0000000010000000
#define  CCTL_OUTMOD_RST     0b0000000010100000
#define  CCTL_OUTMOD_TGL_SET 0b0000000011000000
#define  CCTL_OUTMOD_RST_SET 0b0000000011100000

// Interrupt Enable
#define  CCTL_CCIE_OFF       0b0000000000000000
#define  CCTL_CCIE_ON        0b0000000000010000

// CC Input
#define  CCTL_CCI            0b0000000000000000

// Capture Overflow
#define  CCTL_COV_FALSE      0b0000000000000000
#define  CCTL_COV_TRUE       0b0000000000000010

// Interrupt Flag
#define  CCTL_FLAG_OFF       0b0000000000000000
#define  CCTL_FLAG_ON        0b0000000000000001